                                            <div id="collapseOne" class="accordion-collapse collapse show" aria-labelledby="headingOne" data-bs-parent="#accordionExample">
                                                <div class="accordion-body has-text-align-left">
                                                    <p>Que, la Constitución de la República (CRE), en el artículo 226
                                                        determina que cualquier persona que ejerza potestad estatal,
                                                        únicamente podrá realizarla en apego a las competencias y
                                                        facultades determinadas en la propia Constitución como en la
                                                        ley.</p>
                                                    <p>Que, el artículo 227 de la CRE, señala que “la administración
                                                        pública constituye un servicio a la colectividad que se rige por
                                                        los principios de eficacia, eficiencia, calidad, jerarquía,
                                                        desconcentración, descentralización, coordinación,
                                                        participación, planificación, transparencia y evaluación”.<br>
                                                        Que, el Artículo 264 de la CRE, establece: “Los gobiernos
                                                        municipales tendrán las siguientes competencias exclusivas sin
                                                        perjuicio de otras que determine la ley: (…) 6. Planificar,
                                                        regular y controlar el tránsito y el transporte público dentro
                                                        de su territorio cantonal. (…)”.</p>
                                                    <p>Que, el inciso segundo del artículo 315 de la CRE, dispone que el
                                                        Estado constituirá empresas públicas para la gestión de sectores
                                                        estratégicos, la prestación de servicios públicos, el
                                                        aprovechamiento sustentable de recursos naturales o de bienes
                                                        públicos, el aprovechamiento sustentable de recursos naturales o
                                                        de bienes públicos y el desarrollo de otras actividades
                                                        económicas.</p>
                                                    <p>Que, la CRE en su artículo 394, expresa que el Estado garantizará
                                                        la libertad del transporte terrestre, aéreo, marítimo y fluvial
                                                        dentro del territorio nacional, sin privilegios de ninguna
                                                        naturaleza. La promoción del transporte público masivo y la
                                                        adopción de una política de tarifas diferenciadas de transporte
                                                        serán prioritarias. El Estado regulará entre otros, el
                                                        transporte terrestre”.</p>
                                                    <p>Que, la letra f) del artículo 55 del Código Orgánico de
                                                        Organización Territorial, Autonomía y Descentralización
                                                        (COOTAD), determina como competencia exclusiva de los gobiernos
                                                        autónomos descentralizados municipales la de planificar, regular
                                                        y controlar el tránsito y el transporte terrestre dentro de su
                                                        circunscripción territorial.</p>
                                                    <p>Que, el artículo 130 ibídem establece que les corresponde a los
                                                        gobiernos autónomos descentralizados municipales de forma
                                                        exclusiva planificar, regular y controlar el tránsito, el
                                                        transporte y la seguridad vial, dentro de su territorio
                                                        cantonal. La rectoría general del sistema nacional de tránsito,
                                                        transporte terrestre y seguridad vial corresponderá al
                                                        Ministerio del ramo, que se ejecuta a través del organismo
                                                        técnico nacional de la materia.</p>
                                                    <p>Que, el artículo 277 del COOTAD, establece en su parte pertinente
                                                        que los gobiernos municipales pueden adoptar como una de las
                                                        modalidades de gestión, la creación de empresas públicas siempre
                                                        que esta forma de organización convenga más a sus intereses y a
                                                        los de la ciudadanía; garantice una mayor eficiencia y mejore
                                                        los niveles de calidad en la prestación de servicios públicos de
                                                        su competencia o en el desarrollo de otras actividades de
                                                        emprendimiento.</p>
                                                    <p>Que, la Ley Orgánica de Empresas Públicas (LOEP), publicada en el
                                                        Registro Oficial No. 48 del 16 de octubre del 2009, en el
                                                        artículo 1, prevé que: “Las disposiciones de la presente Ley
                                                        regulan la constitución, organización, funcionamiento, fusión,
                                                        escisión y liquidación de las empresas públicas, que no
                                                        pertenezcan al sector financiero y que actúen en el ámbito
                                                        internacional, nacional, regional, provincial o local; y,
                                                        establecen los mecanismos de control económico, administrativo,
                                                        financiero y de gestión que se ejercerán sobre ellas, de acuerdo
                                                        a lo dispuesto por la Constitución de la República”.</p>
                                                    <p>Que, la LOEP establece en su artículo 16 “La Administración del
                                                        Talento Humano de las empresas públicas corresponde al Gerente
                                                        General o a quien éste delegue expresamente”.</p>
                                                    <p>Que, el segundo inciso del artículo 17 establece que: “El
                                                        Directorio, en aplicación de lo dispuesto por esta Ley, expedirá
                                                        las normas internas de administración del talento humano, en las
                                                        que se regularán los mecanismos de ingreso, ascenso, promoción,
                                                        régimen disciplinario, vacaciones y remuneraciones para el
                                                        talento humano de las empresas públicas.”</p>
                                                    <p>Que, el artículo 31 del Código Orgánico de Entidades de Seguridad
                                                        Ciudadana y Orden Público (COESCOP), determina: “Todo proceso de
                                                        selección de personal para las entidades previstas en este
                                                        Código requiere una planificación previa que establezca y
                                                        justifique las necesidades específicas de talento humano que se
                                                        deben satisfacer. La selección puede desarrollarse a nivel
                                                        nacional, provincial o de las respectivas zonas de planificación
                                                        del país.</p>
                                                    <p>Los cupos de ingreso anual a las entidades de seguridad se
                                                        definirán considerando sus requerimientos en los territorios
                                                        donde operen y de acuerdo con las vacantes previstas, dando
                                                        prioridad a los candidatos o candidatas que sean de origen o
                                                        tengan domicilio civil o residencia familiar en las
                                                        circunscripciones territoriales donde existan dichas vacantes.
                                                        El proceso será́ dirigido y gestionado por el órgano competente
                                                        de cada una de las entidades previstas en este Código”.</p>
                                                    <p>Que, el artículo 32 del COESCOP, establece que los órganos
                                                        competentes de la gestión del talento humano elaborarán los
                                                        perfiles requeridos para el ingreso de los y las aspirantes para
                                                        integrarse como servidoras o servidores públicos de las
                                                        entidades de seguridad previstas en este Código.</p>
                                                    <p>Que, el artículo 27 ibídem señala: “Los Cuerpos de Agentes
                                                        Civiles de Tránsito son los órganos de ejecución operativa
                                                        municipal o metropolitana en materia de vigilancia y control de
                                                        tránsito en las vías de sus respectivas circunscripciones
                                                        territoriales”;</p>
                                                    <p>Que, el artículo 221 del COESCOP define como aspirantes a las
                                                        personas que se incorporen a los cursos que imparten las
                                                        instituciones de formación y capacitación en el proceso de
                                                        preselección para el ingreso a las respectivas entidades
                                                        complementarias de seguridad. Las y los aspirantes no formarán
                                                        parte de la estructura orgánica ni jerárquica de dichas
                                                        entidades, ni ostentarán la calidad de servidores o servidoras
                                                        hasta haber aprobado el curso de formación o capacitación
                                                        requerida y haber cumplido con todos los requisitos legales para
                                                        el ingreso, así́ como haber formalizado el contrato o
                                                        nombramiento correspondiente. Las y los aspirantes no recibirán
                                                        durante el curso de formación remuneración alguna.</p>
                                                    <p>Que, el artículo 30-1 de la Ley Orgánica de Transporte Terrestre,
                                                        Tránsito y Seguridad Vial (LOTTTSV), establece que “los agentes
                                                        civiles de tránsito, serán servidores públicos especializados
                                                        para realizar el control de tránsito en su jurisdicción. En el
                                                        cumplimiento de sus competencias la Agencia Nacional de
                                                        Regulación y Control del Transporte terrestre, Tránsito y
                                                        Seguridad Vial y los gobiernos autónomos descentralizados
                                                        coordinaran con la Dirección Nacional de Control de Tránsito y
                                                        Seguridad Vial de la Policía Nacional y la Comisión de Tránsito
                                                        del Ecuador los planes de contingencia y seguridad vial”;</p>
                                                    <p>Que, la LOTTTSV, en su artículo 30.2 en su parte pertinente
                                                        establece que el control del tránsito y la seguridad vial será
                                                        ejercido, entre otros, por las autoridades municipales en sus
                                                        respectivas circunscripciones territoriales, a través de las
                                                        Unidades de Control de Transporte Terrestre, Tránsito y
                                                        Seguridad Vial de los Gobiernos Autónomos Descentralizados,
                                                        constituidas dentro de su propia institucionalidad, unidades que
                                                        dependerán operativa, orgánica, financiera y administrativamente
                                                        de éstos, y estarán conformadas por personal civil
                                                        especializado, seleccionado y contratado por el Gobierno
                                                        Autónomo Descentralizado y formado por la Agencia Nacional de
                                                        Regulación y Control del Transporte Terrestre, Tránsito y
                                                        Seguridad Vial.</p>
                                                    <p>Que, el artículo 30-3 ibídem establece que: “Los Gobiernos
                                                        Autónomos Descentralizados (…) Municipales son responsables de
                                                        la planificación operativa del control del transporte terrestre,
                                                        tránsito y seguridad vial, planificación que estará enmarcada en
                                                        las disposiciones de carácter nacional emanadas desde la Agencia
                                                        Nacional de Regulación y Control del Transporte Terrestre,
                                                        Tránsito y Seguridad Vial, y deberán informar sobre las
                                                        regulaciones locales que se legislen”.</p>
                                                    <p>Que, el Reglamento General para la Aplicación de la Ley Orgánica
                                                        de Trasporte Terrestre, Tránsito y Seguridad Vial (RGLOTTTSV) en
                                                        su artículo 21 señala: “La Agencia Nacional de Tránsito es la
                                                        encargada de formar y capacitar a los agentes civiles de
                                                        tránsito que realicen el control del trasporte terrestre,
                                                        tránsito y seguridad vial en los GADs. Para el efecto, la ANT
                                                        podrá, mediante resolución, crear, estructurar y normar su
                                                        propia academia de formación de agentes civiles de tránsito, o
                                                        en su defecto podrá suscribir convenios de cooperación con la
                                                        CTE, para que esta, a través de la Escuela de Formación de
                                                        Oficiales y Tropa (EFOT), o a través de convenios con centros de
                                                        educación superior, capacite a los agentes civiles de tránsito
                                                        que se requieran. La ANT podrá también suscribir convenios de
                                                        cooperación con los GADs para que estos por sí mismos y bajo
                                                        supervisión de la Agencia Nacional de Tránsito formen y
                                                        capaciten a los agentes civiles de tránsito en sus respectivas
                                                        jurisdicciones”;</p>
                                                    <p>Que, el RGLOTTTSV en el artículo 22, especifica: “Toda persona
                                                        que aspire a ser agente civil de tránsito, deberá previamente
                                                        aprobar el curso correspondiente. La Agencia Nacional de
                                                        Tránsito mediante Resolución, normará todo lo relativo a los
                                                        cursos que se deban impartir a los aspirantes”;</p>
                                                    <p>Que, el RGLOTTTSV en el artículo 23, dispone: “La aprobación a
                                                        los cursos correspondientes no garantiza la contratación como
                                                        agente civil de tránsito. Para tales efectos se deberá seguir
                                                        además, los pasos y cumplir los requisitos para el ingreso al
                                                        servicio público que señalen la Ley Orgánica de Servicio Público
                                                        y las demás normas aplicables”;</p>
                                                    <p>Que, el RGLOTTTSV en el artículo 24 indica: “Para el ejercicio de
                                                        las competencias de control señaladas en la ley, los GADs
                                                        deberán previamente contar con agentes civiles de tránsito
                                                        debidamente capacitados que garanticen la correcta prestación
                                                        del servicio de control de trasporte terrestre, tránsito y
                                                        seguridad vial. Las nóminas de los agentes civiles de tránsito
                                                        contratados serán enviadas a la ANT por los GADs. En ningún caso
                                                        se podrá contratar como agente a quien no hubiera aprobado los
                                                        cursos correspondientes. (…)”.</p>
